public class Displayable{
    public Displayable(){
        
    }

    public void setInvisible(){
        System.out.println("set invisible");
        
    }

    public void setVisible(){
        System.out.println("set visible: 1");
        
    }

    public void setType(char t){
        System.out.println("set type: " + t);
        
    }

    public void setIntValue(int v){
        System.out.println("set int value: " + v);
        
    }

    public void setPosX(int x){
        System.out.println("set pos x: " + x);
        
    }

    public void setPosY(int y){
        System.out.println("set pos y: " + y);
        
    }

    public void setWidth(int x){
        System.out.println("set width: " + x);
        
    }

    public void setHeight(int y){
        System.out.println("set height: " + y);
        
    }
}